class MathOperations:
    def __init__(self):
        pass

    def add(self, number: int, accumulator: int) -> int:
        return number + accumulator
    
    def subtract(self, number: int, accumulator: int) -> int:
        return number - accumulator

    def multiply(self, number: int, accumulator: int) -> int:
        return number * accumulator
    
    def divide(self, number: int, accumulator: int) -> int:
        return number / accumulator
